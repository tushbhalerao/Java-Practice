public class ConditionalStatementSwitchEX8 {
    public static void main(String args[]) {
      int i=10;
      
      switch(i){
         case 5:
              System.out.println("five");
            break;
          case 10:
              System.out.println("ten");
            break;
          case 15:
              System.out.println("fifteen");
            break;
          case 20:
              System.out.println("twenty");
            break;
          default:
              System.out.println("Default");
            break; 
      }
    }
}