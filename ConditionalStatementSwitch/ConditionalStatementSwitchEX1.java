public class ConditionalStatementSwitchEX1 {
    public static void main(String args[]) {
      int i=10;
      
      switch(i){
          case 5:
              System.out.println(i);
          break;
          case 10:
              System.out.println(i);
          break;
          case 15:
              System.out.println(i);
          break;
          case 20:
              System.out.println(i);
          break;
      }
    }
}